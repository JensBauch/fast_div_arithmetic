intrinsic Sort(L::SeqEnum,SortL::SeqEnum)->SeqEnum
{Gives Bijection between L and SortL}

	Per:=[];
	for i in [1..#L] do
		j:=Position(L,SortL[i]);
		Append(~Per,j);
		L[j]:=-2;
	end for;

	return Per;

end intrinsic;

