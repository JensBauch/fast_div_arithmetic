intrinsic Sort(L::List)->List
{Sorts a list}

	Level1:=[**];
	L1:=[i[1]: i in L];
	Sort1:=Sort(L1);
	for i in Sort1 do
		Append(~Level1,L[Position(L1,i)]);
		L1[Position(L1,i)]:=0;
	end for;
	Level2:=[**];
	j:=1;
	for p in SetToSequence(Set(Sort1)) do
		TempP:=[];
		while p eq  Sort1[j] do
			Append(~TempP,Level1[j]);	
			if j eq #Sort1 then break; end if;
			j:=j+1;
		end while;
		L2:=[i[2]: i in TempP];
		Sort2:=Sort(L2);
	
		for i in Sort2 do
			Append(~Level2,TempP[Position(L2,i)]);
			L2[Position(L2,i)]:=0;
		end for;
	end for;

	return Level2;
end intrinsic;

