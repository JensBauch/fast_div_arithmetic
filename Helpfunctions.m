intrinsic IntegerToBinary(n::RngIntElt)->SeqEnum
{Compute binary representation of an integer n}
	L:=[];
	if n lt 0 then 
		n:=Abs(n);
		help:=-1;
	else;
		help:=1;
	end if;

	while n gt 0 do
		Append(~L,help*Integers()!(n mod 2));
		n:=n div 2;
	end while;

	return L;

end intrinsic;


