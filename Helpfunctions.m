intrinsic 'in'(L1::List,L2::List)->BoolElt
{Some special helpfunction}

	if #L2 eq 0 or #L1 eq 0 then return false,0; end if;
	for i in [1..#L2] do
		if L1[1] eq L2[i][1] and L1[2] eq L2[i][2] then return true,i; end if;  
	end for;

	return false,0;

