intrinsic 'mod'(alpha:: FldFunElt, p:: RngUPolElt)->FldFinElt
{Realizes the reduction map ZK--> ZK/pZK}

	R:=Parent(p);
	num:=Numerator(alpha);
	den:=Denominator(alpha);
	return (Parent(alpha)![R!i mod p:i in Eltseq(num)])/den;

end intrinsic;


